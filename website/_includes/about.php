		<h1>What can I expect from my first treatment session?</h1>
		<p class="p-1">Intake form: You will fill out a patient intake form, including medical and pain history.</p>

		<p>Attire: Please bring loose shorts or a bathing suit to your session. For women, please bring sports bra, bathing suit top, or loose tank top.</p> 

		<p>Please do not use any lotions or oils on the day of your appointment. Your skin needs to be clean and dry for this technique.</p>

		<p>Evaluation and treatment: Your first session will include a discussion of your current condition or problem, your health history, include any previous injuries or trauma.  The evaluation will include a whole-body assessment of your mobility and posture, areas of tenderness and restrictions. Treatment will include a myofascial release therapy session along with instructions in home stretches to help you maintain the benefits from the treatment session.</p>

		<p>I offer Myofascial Release Therapy in Golden, Colorado. Easy access from Denver and Evergreen Mountain Communities, one block east of the Jefferson County Administrative complex.</p>

