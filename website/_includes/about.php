		<h1 class="bio-head">Cindy Petrinovic Miller OTR/L, CHT</h1>
		<p class="p-1">Cindy Petrinovic-Miller is an Occupational Therapist and Certified Hand (and Upper) Extremity Therapist whom has specialized in treating the upper extremity for over 30 years. Her extensive experience in a variety of out patient settings afforded her the ability to recognize that not every gets better with traditional outpatient therapy. Frequently patients where left with structural limitations and pain that were not addressed by standard outpatient therapy protocols and care.</p>

		<p>Cindy began incorporating myofascial release techniques into her own patient care over 20 years ago when she was first introduced to myofascial therapy by a colleague. Over the years patients have requested that Cindy use myofascial release therapy in other areas of the body including the neck, torso, lower back, abdominal area, pelvis and lower extremities. Most of these patients where already in other therapies that where not addressing their myofascial restrictions and they had continuing pain and functional limitations Cindy decided to further her training in myofascial release techniques in order to offer the same pain-relieving techniques to the whole body.</p>
